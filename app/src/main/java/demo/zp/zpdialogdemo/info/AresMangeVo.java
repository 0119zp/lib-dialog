package demo.zp.zpdialogdemo.info;

public class AresMangeVo{
	String name;
	String isRed;
	String fun;

	public AresMangeVo() {
	}

	public AresMangeVo(String name, String isRed) {
		this.name = name;
		this.isRed = isRed;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getIsRed() {
		return isRed;
	}

	public void setIsRed(String isRed) {
		this.isRed = isRed;
	}

	public String getFun() {
		return fun;
	}

	public void setFun(String fun) {
		this.fun = fun;
	}
}
